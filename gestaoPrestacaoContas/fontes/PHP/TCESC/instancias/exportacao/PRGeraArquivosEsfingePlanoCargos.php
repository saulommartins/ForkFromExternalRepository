       if ($rsAtualizacaoCargo->arElementos[$i]['diff_vagas'] > 0) {
          $rsAtualizacaoCargo->arElementos[$i]['aumento_vagas'] = $rsAtualizacaoCargo->arElementos[$i]['diff_vagas'];
          $rsAtualizacaoCargo->arElementos[$i]['reducao_vagas'] = 0;
       } else {
          $rsAtualizacaoCargo->arElementos[$i]['aumento_vagas'] = 0;
          $rsAtualizacaoCargo->arElementos[$i]['reducao_vagas'] = -$rsAtualizacaoCargo->arElementos[$i]['diff_vagas'];
       }
