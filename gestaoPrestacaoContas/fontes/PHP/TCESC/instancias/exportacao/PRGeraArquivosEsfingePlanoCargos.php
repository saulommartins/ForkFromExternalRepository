    $obTPessoalCargo->setDado( 'exercicio'   , $arFiltroRelatorio['stExercicio'] );
    $obTPessoalCargo->setDado( 'cod_entidade', implode(",", $arFiltroRelatorio['inCodEntidade'] ) );
