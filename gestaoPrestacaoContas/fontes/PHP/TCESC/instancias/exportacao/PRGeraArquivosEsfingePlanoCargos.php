          $rsAtualizacaoCargo->arElementos[$i]['aumento_vagas_ocupadas'] = $rsAtualizacaoCargo->arElementos[$i]['diff_vagas_ocupadas'];
          $rsAtualizacaoCargo->arElementos[$i]['reducao_vagas_ocupadas'] = 0;
